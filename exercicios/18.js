function numeroExtenso(numero) {

    switch (numero) {
        case 0:
            console.log("zero")
            break
        case 1:
            console.log("um")
            break
        case 2:
            console.log("dois")
            break
        case 3:
            console.log("tres")
            break
        case 4:
            console.log("quatro")
            break
        case 5:
            console.log("cinco")
            break
        default:
            console.log('insira uma valor valido')
            break

    }
}

numeroExtenso(8)